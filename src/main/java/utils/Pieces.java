package utils;

/**
 * This enum represents the types of chess pieces in the game.
 * It includes the standard pieces (PAWN, BISHOP, KNIGHT, ROOK, QUEEN, KING),
 * their black counterparts (PAWN_BLACK, BISHOP_BLACK, KNIGHT_BLACK, ROOK_BLACK, QUEEN_BLACK, KING_BLACK),
 * and an EMPTY value.
 */
public enum Pieces {
    PAWN, BISHOP, KNIGHT, ROOK, QUEEN, KING,
    PAWN_BLACK, BISHOP_BLACK, KNIGHT_BLACK, ROOK_BLACK, QUEEN_BLACK, KING_BLACK,
    EMPTY
}
